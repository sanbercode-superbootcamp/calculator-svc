export function add(n1: number, n2: number): number {
    return n1 + n2;
}

export function minus(n1: number, n2: number): number {
    return n1 - n2;
}

export function multi(n1: number, n2: number): number {
    return n1 * n2;
}

export function div(n1: number, n2: number): number {
    return n1 * n2;
}