export function plus(n1: number, n2: number): number {
    return n1+n2;
}

export function minus(n1: number, n2: number): number {
    return n1-n2;
}

export function multiply(n1: number, n2:number): number {
    return n1*n2;
}

export function divide(n1:number, n2:number): number {
    return n1/n2;
}