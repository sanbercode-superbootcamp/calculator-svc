export function tambah(n1: number, n2: number): number{
    return n1 + n2;
}

export function kurang(n1: number, n2: number): number{
    return n1 - n2;
}

export function kali(n1: number, n2: number): number{
    return n1 * n2;
}

export function bagi(n1: number, n2: number): number{
    return n1 / n2;
}